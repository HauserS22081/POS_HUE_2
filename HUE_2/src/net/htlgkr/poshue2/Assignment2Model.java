package net.htlgkr.poshue2;

public class Assignment2Model {
    private int number;
    private int type;

    public Assignment2Model(int type, int number) {
        this.number = number;
        this.type = type;
    }

    public int getType() {
        return type;
    }

    public int getNumber() {
        return number;
    }


}
